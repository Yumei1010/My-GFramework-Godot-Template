namespace GFrameworkGodotTemplate.scripts.enums.ui;

/// <summary>
///     定义用户界面键值枚举，用于标识不同的UI面板或菜单
/// </summary>
public enum UiKey
{
    /// <summary>
    ///     主菜单界面键值
    /// </summary>
    MainMenu,

    /// <summary>
    ///     保存菜单界面键值
    /// </summary>
    SaveMenu,

    /// <summary>
    ///     加载菜单界面键值
    /// </summary>
    /// <remarks>
    ///     表示加载菜单相关的界面状态或操作类型
    /// </remarks>
    LoadMenu,

    /// <summary>
    ///     选项菜单界面键值
    /// </summary>
    /// <remarks>
    ///     表示选项菜单相关的界面状态或操作类型
    /// </remarks>
    OptionsMenu,

    /// <summary>
    ///     版权信息界面键值
    /// </summary>
    /// <remarks>
    ///     表示显示版权信息的界面状态或操作类型
    /// </remarks>
    Credits,
    HomeUi,
    PauseMenu
}