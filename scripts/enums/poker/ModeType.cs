namespace GFrameworkGodotTemplate.scripts.enums.poker;

/// <summary>
///     定义计算方法类型的键值枚举
///     用于标识和管理不同的计算方法
/// </summary>
public enum ModeType
{
    /// <summary>
    /// 加
    /// </summary>
    Add,
    
    /// <summary>
    /// 减
    /// </summary>
    Subtract,
    
    /// <summary>
    /// 乘
    /// </summary>
    Multiply,
    
    /// <summary>
    /// 除
    /// </summary>
    Divide,
    
    /// <summary>
    /// 模
    /// </summary>
    Modulo,
    
    /// <summary>
    /// 指数幂
    /// </summary>
    Power,
    
    /// <summary>
    /// N次根
    /// </summary>
    NthRoot,
    
    /// <summary>
    /// 绝对值
    /// </summary>
    AbsoluteValue,

    /// <summary>
    /// 阶乘
    /// </summary>
    Factorial,
    
    /// <summary>
    /// 平方根
    /// </summary>
    SquareRoot,
    
    /// <summary>
    /// 向上取整
    /// </summary>
    Ceil,
    
    /// <summary>
    /// 向下取整
    /// </summary>
    Floor
}